package com.ssb.regularQuestion;

public class Inquiry {
	private int num, memberIdx, type, parent, state;
	private int phoneRecv, emailRecv;
	private String category, subject, content, created;
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getMemberIdx() {
		return memberIdx;
	}
	public void setMemberIdx(int memberIdx) {
		this.memberIdx = memberIdx;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getParent() {
		return parent;
	}
	public void setParent(int parent) {
		this.parent = parent;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public int getPhoneRecv() {
		return phoneRecv;
	}
	public void setPhoneRecv(int phoneRecv) {
		this.phoneRecv = phoneRecv;
	}
	public int getEmailRecv() {
		return emailRecv;
	}
	public void setEmailRecv(int emailRecv) {
		this.emailRecv = emailRecv;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	
	
}
